package OCT_18102024_Switch;

public class Ex_91 {
    public static void main(String[] args) {
        char code = 'A';
        switch (code){
            default:
                System.out.println("Hellooooooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
