package OCT_22102024_WhileLoop;

public class Ex_110 {
    public static void main(String[] args) {
        int i=0;
        while(i<10)
        {
            System.out.println(i);
            i++;
        }
        for (int j = 0; j < 10 ; j++) {   //same for above code
            System.out.println(j);
        }
    }
}
