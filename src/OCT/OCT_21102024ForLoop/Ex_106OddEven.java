package OCT.OCT_21102024ForLoop;

public class Ex_106OddEven {
    public static void main(String[] args) {
        for (int i=0;i<=50;i++){
            if(i%2==0){
                System.out.println( i+ ": Number is even");
            }
            else
            {
                System.out.println(i+": Number is Odd");
            }
        }
    }
}
