package OCT.OCT_22102024_WhileLoop;

public class Ex_118 {
    public static void main(String[] args) {
        int i=0;
        while (i<10){
            System.out.println("print i:" +i);
            i++;
        }
        int a=0;
        do{
            System.out.println("print a :" +a);
            a++;
        }while(a<10);
    }
}
