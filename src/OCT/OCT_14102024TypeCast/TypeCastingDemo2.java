package OCT.OCT_14102024TypeCast;

public class TypeCastingDemo2
{
    public static void main(String[] args) {
        double doubleNum = 9.78;       // double type
        int intNum = (int) doubleNum;  // explicit casting from double to int

        System.out.println("Double: " + doubleNum); // Output: 9.78
        System.out.println("Integer: " + intNum);   // Output: 9
    }
}
